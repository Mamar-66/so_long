#include <unistd.h>

int	ft_putstr(char *s)
{
	size_t	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != '\0')
		{
			write(1, &s[i], 1);
			i++;
		}
	}
	else
	{
		write(1, "(null)", 6);
		i += 6;
	}
	return (i);
}
