#include <unistd.h>
#include <stdio.h>

int	ft_putnbr(long n)
{
	int	i;

	i = 0;
	if (n < 0)
	{
		i++;
		write(1, "-", 1);
		i += ft_putnbr(n * (-1));
	}
	if (n > 9)
	{
		i += ft_putnbr(n / 10);
		i += ft_putnbr(n % 10);
	}
	else if (n >= 0 && n < 10)
	{
		i++;
		n = n + '0';
		write(1, &n, 1);
	}
	return (i);
}
